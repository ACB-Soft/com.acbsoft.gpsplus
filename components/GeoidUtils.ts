export const getGeoidUndulation = (lat: number, lng: number): number => {
  // Normalize lng to -180 to 180
  let lon = ((lng + 180) % 360);
  if (lon < 0) lon += 360;
  lon -= 180;

  const row = (90 - lat) / 10;
  const col = (lon + 180) / 10;

  const r0 = Math.floor(row);
  const r1 = Math.min(r0 + 1, 18);
  const c0 = Math.floor(col);
  const c1 = (c0 + 1) % 36;

  const dr = row - r0;
  const dc = col - c0;

  // Bilinear interpolation
  const n00 = EGM96_GRID[r0][c0];
  const n01 = EGM96_GRID[r0][c1];
  const n10 = EGM96_GRID[r1][c0];
  const n11 = EGM96_GRID[r1][c1];

  const n = (1 - dr) * (1 - dc) * n00 +
            (1 - dr) * dc * n01 +
            dr * (1 - dc) * n10 +
            dr * dc * n11;

  return n;
};

export const convertToMSL = (lat: number, lng: number, ellipsoidalHeight: number | null): number | null => {
  if (ellipsoidalHeight === null) return null;
  const undulation = getGeoidUndulation(lat, lng);
  return ellipsoidalHeight - undulation;
};
